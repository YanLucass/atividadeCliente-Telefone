public class Telefone {
    private int ddd;
    private int numero;

    public Telefone(int ddd, int numero) {

        this.ddd = ddd;
        this.numero = numero;

    }

    public int getdd() {
        return ddd;
    }

    public void setdd(int ddd) {

        this.ddd = ddd;

    }
    
    public int getnumero() {
        return numero;
    }

    public void setnumero() {
        this.numero = numero;
    }


}


